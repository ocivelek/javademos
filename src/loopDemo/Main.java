package loopDemo;

public class Main {

	public static void main(String[] args) {
		//FOR
		int i;
		for(i=1;i<10;i++) {
			System.out.println(i);
		}
		System.out.println("Döngü Bitti");
		for(i=0;i<=10;i+=2) {
			System.out.println(i);
		}
		System.out.println("Dögü Bitti");
	}

}
